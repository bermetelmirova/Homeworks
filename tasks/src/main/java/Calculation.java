public class Calculation {
    public static double plus(double m, double n) {
        return m + n;
    }
    public static double minus(double m, double n) {
        return m - n;
    }
    public static double divide(double m, double n) {
        return m / n;
    }
    public static double increase(double m, double n) {
        return m * n;
    }
}
